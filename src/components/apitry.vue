<template>
  <div>
    <h1>bitcoiner</h1>
    {{ info }}
  </div>
</template>

<script>
import Vue from 'vue';

import axios from 'axios';

new Vue({
  el: '#app',
  data () {
    return {
      info: null
    }
  },
  mounted () {
    axios
      .get('https://api.coindesk.com/v1/bpi/currentprice.json')
      .then(response => (this.info = response))
  }
})
</script>

